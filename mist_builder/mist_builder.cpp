// Construct a dio_app
mist_builder::mist_builder( const FXString &name, const FXString &vendor ) : base( name, vendor )
{
	cursors.push_back( new FXCursor( this, CURSOR_ARROW ) );
	cursors.push_back( new FXCURCursor( this, drag ) );
	cursors.push_back( new FXCURCursor( this, dragging ) );
}

// Construct a dio_app
mist_builder::~mist_builder( )
{
	std::vector< FXCursor * >::iterator ite = cursors.begin( );
	for( ; ite != cursors.end( ) ; ite++ )
	{
		delete *ite;
	}
}

void mist_builder::create( )
{
	for( size_type i = 0 ; i < cursors.size( ) ; i++ )
	{
		cursors[ i ]->create( );
	}

	base::create( );
}


